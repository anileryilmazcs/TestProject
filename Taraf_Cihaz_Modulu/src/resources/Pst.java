package resources;

public class Pst extends Cihaz{

	private int pstA;
	private int pstB;
	private int pstC;
	
	public Pst() {
		super();
	}

	public Pst(int pstA, int pstB, int pstC) {
		super();
		this.pstA = pstA;
		this.pstB = pstB;
		this.pstC = pstC;
	}

	public int getPstA() {
		return pstA;
	}

	public void setPstA(int pstA) {
		this.pstA = pstA;
	}

	public int getPstB() {
		return pstB;
	}

	public void setPstB(int pstB) {
		this.pstB = pstB;
	}

	public int getPstC() {
		return pstC;
	}

	public void setPstC(int pstC) {
		this.pstC = pstC;
	}

	
	
}
